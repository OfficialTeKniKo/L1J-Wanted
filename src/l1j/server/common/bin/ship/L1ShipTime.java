package l1j.server.common.bin.ship;

public class L1ShipTime {
	private int hour, stay, drive, leave, arrive;
	
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
	public int getStay() {
		return stay;
	}
	public void setStay(int stay) {
		this.stay = stay;
	}
	public int getDrive() {
		return drive;
	}
	public void setDrive(int drive) {
		this.drive = drive;
	}
	public int getLeave() {
		return leave;
	}
	public void setLeave(int leave) {
		this.leave = leave;
	}
	public int getArrive() {
		return arrive;
	}
	public void setArrive(int arrive) {
		this.arrive = arrive;
	}
}

